def kubik_angka(angka):
    # Mengembalikan nilai kubik dari suatu angka
    return angka ** 3

def cek_habis_dibagi_tiga(angka):
    # Mengecek apakah angka habis dibagi 3
    if angka % 3 == 0:
        # Jika habis dibagi 3, jalankan fungsi pertama
        hasil_kubik = kubik_angka(angka)
        return hasil_kubik
    else:
        # Jika tidak habis dibagi 3, kembalikan tulisan "False"
        return False

# Contoh penggunaan
angka_input = int(input("Masukkan angka: "))

hasil_cek = cek_habis_dibagi_tiga(angka_input)

if hasil_cek is not False:
    print(f"Hasil kubik dari {angka_input} adalah: {hasil_cek}")
else:
    print(f"{angka_input} false")