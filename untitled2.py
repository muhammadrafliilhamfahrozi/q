def hitung_vokal_dan_konsonan(kalimat):
    # Inisialisasi jumlah vokal dan konsonan
    jumlah_vokal = 0
    jumlah_konsonan = 0

    # Konversi kalimat menjadi huruf kecil untuk memudahkan penghitungan
    kalimat = kalimat.lower()

    # Loop melalui setiap karakter dalam kalimat
    for karakter in kalimat:
        # Periksa apakah karakter merupakan huruf
        if karakter.isalpha():
            # Periksa apakah huruf tersebut merupakan vokal atau konsonan
            if karakter in 'aeiou':
                jumlah_vokal += 1
            else:
                jumlah_konsonan += 1

    # Tampilkan hasil
    print(f"Jumlah huruf vokal: {jumlah_vokal}")
    print(f"Jumlah huruf konsonan: {jumlah_konsonan}")

